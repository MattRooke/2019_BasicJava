package week4;

public class Sandwich {
    String mainIngredient;
    String breadType;
    double price;

    public String getMainIngredient() {
        return mainIngredient;
    }

    public void setMainIngredient(String input) {
        mainIngredient = input;
    }

    public String getBreadType() {
        return breadType;
    }

    public void setBreadType(String input) {
        breadType = input;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double input) {
        price = input;
    }
}
